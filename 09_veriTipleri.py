sampleInteger =1213232424234324
print(sampleInteger) # 1213232424234324 (10 luk sistem)
print(type(sampleInteger)) # <class 'int'> type keyword u data tipini (class'ini) verir.

sampleBinary =0B1010 # veya 0b1010 (2 li sistem başta 0 sonra binary nin b si)
print(sampleBinary) # 10 (10 luk sistem)
print(type(sampleBinary)) # <class 'int'>

sampleOctave = 0o543 # (8 li sistem 0 ile başlar sonrasında o ile devam eder 8 (hariç) e kadar olan sayılar)
print(sampleOctave) # 355 (10 luk sistem)
print(type(sampleOctave)) # <class 'int'>

sampleHexadecimal = 0x2F3  # veya 0X (0 dan 9 a kadar olan sayılar ve a,b,c,d,e,f harfleri rakam olara kullanılabilir)
print(sampleHexadecimal) # 755 (10 luk sistem)
print(type(sampleHexadecimal)) # <class 'int'>

sampleFloat = 4.2
print(sampleFloat) # 4.2 (float)
print(type(sampleFloat)) # <class 'float'>

sampleBigFloat = 1.8E23 # (1.8 * 10 üzeri 23)
print(sampleBigFloat) # 1.8e+23
print(type(sampleBigFloat)) # <class 'float'>

sampleSmallFloat = 2.3E-54
print(sampleSmallFloat) # 2.3e-54
print(type(sampleSmallFloat)) # 2.3e-54